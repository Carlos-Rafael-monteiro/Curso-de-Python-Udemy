# if /  elif............ / else
# if = se
# elif = se não se
# else = se não

condicao1 = False
condicao2 = True
condicao3 = True
condicao4 = True


if condicao1:
    print('codigo para condição 1')
elif condicao2:
    print('codigo para condição 2')
elif condicao3:
    print('codigo para condição 3')
elif condicao4:
    print('codigo para condição 4')


else:
    print('nenhuma condição foi satisfeita')