'''
Docstring: Aula 3 - Strings
Python = Lingue
Tipo de tipagem: Dinâmica / Forte
str -> string -> texto
strings são textos que estão entre aspas
'''
print(1234)

# Aspas simples
print('Carlos Rafael')
print(1, 'Carlos "Rafael"')

# Aspas duplas
print("Carlos Rafael")
print(2, "Carlos 'Rafael'")

# Escape
print("Carlos \"Rafael\"")

# r
print(r"Carlos \"Rafael\"")
